import './globals.css'
import { IBM_Plex_Sans } from 'next/font/google'
import { createServerComponentClient } from "@supabase/auth-helpers-nextjs"
import { cookies } from "next/headers"


// components
import SessionTimeoutHandler from './components/SessionTimeoutHandler'

// tell next to render dynamic and not static pages at build time
export const dynamic = 'force-dynamic'

// global messages
import { MessageProvider } from './context/MessageContext'
import MessageBanner from './components/MessageBanner'

const ibmps = IBM_Plex_Sans({ subsets: ['latin'], weight: ['100', '200', '300', '400', '500', '600', '700' ] });

// metadata
export const metadata = {
  title: 'My Portfolio',
  description: 'A portfolio showcasing my work generated by create next app'
}

export default async function RootLayout({ children }) {
const supabase = createServerComponentClient({ cookies })
const { data: { user } } = await supabase.auth.getUser()

  return (
    <html lang="en">
      <body className={ibmps.className}>
        <div>
          <MessageProvider>
            <MessageBanner />
            {children}
            {user && <SessionTimeoutHandler />}
          </MessageProvider>
        </div>
      </body>
    </html>
  )
}
